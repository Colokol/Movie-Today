import UIKit

extension UIColor {
    static var background: UIColor {
        return UIColor(red: 31/255, green: 29/255, blue: 43/255, alpha: 1.0)
    }
    
    static var whiteGray: UIColor {
        return UIColor(red: 235/255, green: 235/255, blue: 239/255, alpha: 1.0)
    }
    static var soft: UIColor {
        return UIColor(red: 37/255, green: 40/255, blue: 54/255, alpha: 1.0)
    }
}
